#include "libft.h"

void	ft_splitdel(char ***s)
{
	int		i;

	i = 0;
	while (*s && (*s)[i])
	{
		ft_strdel(&(*s)[i]);
		i++;
	}
	free(*s);
	*s = NULL;
}